macro_rules! def_instruction_dispatch {
    ($dollar:tt, [$($enumerant:ident,)*]) => {
        macro_rules! instruction_dispatch {
            ($dollar instruction:expr, $dollar v:ident, $dollar case:expr) => {
                match $dollar instruction {
                    $(spirv_parser::Instruction::$enumerant($dollar v) => $dollar case,)*
                }
            };
        }
    };
}

